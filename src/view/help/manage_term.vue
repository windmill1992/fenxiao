                <p class="sub">4.9  经销商退换货（各国视当地法律不同可有所调整）：将按照具体的《众康科诺平台退换货规则》进行。</p>
                <p class="title bold">5.知识产权的使用</p>
                <p class="sub">5.1  经销商确认，所有众康科诺平台上所使用的商标、版权、奖章、徽章代表符号、设计、资料和信息（如书籍、声音资料、影像资料，宣传资料等）以及一切众康科诺使用而与众康科诺平台及产品有关联的工业技巧及知识（以下统称“知识产权”）均为众康科诺或其合作方所有，经销商仅在众康科诺或其合作方明确授权的范围和期限内使用众康科诺平台上相关的知识产权，不得以任何其他的方式使用，如经销商不得有以下行为：</p>
