                <p class="sub1">5.1.3  在任何媒体刊登或播出有关众康科诺业务或众康科诺平台产品的广告，但由众康科诺同意的除外。</p>
                <p class="sub1">5.1.4  以baie或众康科诺、众康科诺的音译，以合作方的名称、商标或音译或其他由此衍生的名称作为商标进行注册或作为任何机构或生意的名称，或授予他人使用此名称。</p>
                <p class="sub1">5.1.5  注册或使用与众康科诺官网、商标、公众号等相近似或容易引起混淆的内容。</p>
                <p class="sub1">5.1.6  经销商可使用众康科诺提供的印刷品，但仅可供其经营众康科诺平台业务之用，私自复制、改编、改造或发行。</p>
                <p class="sub1">5.1.7  通过存盘保留任何知识产权或商业秘密。</p>
                <p class="sub">5.2  推广行为</p>
                <p class="sub1">5.2.1  经销商符合众康科诺平台规定的前提下从事平台事业。若因经销商违反规定导致众康科诺的声誉受损（如：受到传媒的负面报道），甚至引起法律行动，经销商须承担因此而造成的一切后果（如：承担损失，并赔偿众康科诺及其合作伙伴因此而遭受的一切损失）。</p>
                <p class="sub1">5.2.2  如经销商使用众康科诺提供的推广素材而遭到第三方投诉，政府调查众康科诺或引起法律诉讼，经销商应立即通知众康科诺平台。经销商应保证其所使用的素材和资料是最新的，否则经销商应自行承担未使用最新的素材或资料而产生的后果和责任。</p>
                <p class="sub1">5.2.3  经销商可将在众康科诺提供的文宣资料及官方印刷品上刊登的文章，转载或分发给合作伙伴成员，但须全部正确转载并注明出处，众康科诺出版及制作的一切刊物及视听资料均受当地国著作权法的保障，经销商或其他人士未经众康科诺的书面许可，不得翻印或复印其全部或任何部分。</p>
