                <p class="sub">4.2  经销商不得错误描述或推广众康科诺产品及服务的价钱、规格、质量、性能、成份、款式、型号、产地或供应情况等信息。</p>
                <p class="sub">4.3  经销商须保证与众康科诺平台保持一致，不得恶意在市场上散播对众康科诺发展不利或影响众康科诺声誉的言论，不得将任何非众康科诺产品或服务伪称为众康科诺产品或服务加以推广。</p>
                <p class="sub">4.4  推荐人推荐的经销商，按标准和流程成功注册众康科诺系统，并确认和同意众康科诺《经营守则》后，经推荐人审核通过者，即可成为众康科诺平台经销商，即成为推荐人的一阶合作伙伴。经销商有权根据第2条的规定重新推荐已失去众康科诺经销商资格的人重新加入。</p>
